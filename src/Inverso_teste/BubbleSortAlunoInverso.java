package Inverso_teste;

public class BubbleSortAlunoInverso {
    public static void bubbleSort(Aluno[] arr) {
        int n = arr.length;
        boolean trocou;
        for (int i = 0; i < n - 1; i++) {
            trocou = false;
            for (int j = 0; j < n - i - 1; j++) {
                if (arr[j].nota < arr[j + 1].nota) {
                    Aluno temp = arr[j];
                    arr[j] = arr[j + 1];
                    arr[j + 1] = temp;
                    trocou = true;
                }
            }
            if (!trocou) break;
        }
    }
}